function openMenu() {
  var x = document.getElementById("contacts");
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }
}
function openMainMenu() {
  var x = document.getElementById("mainNav");
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }
}
